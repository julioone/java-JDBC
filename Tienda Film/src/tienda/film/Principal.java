    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        Connection connection = null;
        Statement statement = null;
        ResultSet resultset = null;
        
        
        try

        {
            
            connection  = DriverManager.getConnection(TiendaFilm.DATABASE_URL,"root","tucamaesmia1");
            statement = connection.createStatement();
            String datoUser = textUser.getText();
            String datoPass = textPass.getText();
            resultset = statement.executeQuery("SELECT staff.first_name, store.store_id, address.address FROM staff INNER JOIN store ON staff.store_id = store.store_id INNER JOIN address ON store.address_id = address.address_id WHERE staff.username = '"+datoUser+"' and staff.password = '"+datoPass+"'");
            int count = 0;
            while (resultset.next()) {
                this.nombreUsuario= resultset.getString("staff.first_name");
                this.direccionTienda = resultset.getString("store.store_id")+resultset.getString("address.address");
                count++;
            }
            if(count == 1)
            {
                //usuario es validado.
                JOptionPane.showMessageDialog(null, "BIenvenido "+this.nombreUsuario+" "+this.direccionTienda+"");
                this.setVisible(false);
                MenuPrincipal ventanaMenu = new MenuPrincipal(this,false);
                ventanaMenu.setVisible(true);
                
                //ventanaPrincipal.setNombreUsuario(this.nombreUsuario);
                //ventanaPrincipal.setDireccionTienda(this.direccionTienda);
                
               
                
            }
            else
            {
                //usuario invalido.
                JOptionPane.showMessageDialog(null, "vuelva a ingresar credenciales");
                textUser.setText(null);
                textPass.setText(null);
            }
            
        }

        catch( SQLException sqlException)

        {

            sqlException.printStackTrace();

        }

        finally

        {

            try

            {

                resultset.close();

                statement.close();

                connection.close();

                

            }

            catch(Exception exception)

            {

                exception.printStackTrace();

                

            }

        }
        
        
        
    }//GEN-LAST:event_btnIngresarActionPerformed

